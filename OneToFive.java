public  class OneToFive {

    public static void main(String[] args) {
        
        int initalNum;
        int squaredNum;

        for (int i = 1; i <= 5; i++) {
            initalNum = i;
            squaredNum = i * i;
            System.out.println(initalNum + " * " + i + " = " + squaredNum);
            
        }
    }
}